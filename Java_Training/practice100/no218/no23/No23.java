package Java_Training.practice100.no218.no23;

public class No23 {
	
	private static String converString(final int n) {
		return String.valueOf(n);
	}
	private static String converString(final float m) {
		return String.valueOf(m);
	}

	public static void main(String[] args){
		System.out.println(converString(1));
		System.out.println(converString(1f));
	}

	

	
	
}